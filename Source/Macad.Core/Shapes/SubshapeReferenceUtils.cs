public static class SubshapeReferenceUtils
{
    /// <summary>
    /// Delegate for adding a named subshape. The callback provides the name, shape, index, and sources of the subshape.
    /// </summary>
    public delegate void NamedSubshapeCallback(string name, TopoDS_Shape shape, int index, TopoDS_Shape[] sources);

    //--------------------------------------------------------------------------------------------------

    #region Create names for Primitives

    /// <summary>
    /// Creates subshape names for a given algorithm and invokes the provided callback for each named subshape.
    /// This is typically used to create names for subshapes generated by primitive generation algorithms which
    /// does not have a source shape.
    /// Supports algorithms based on BRepPrim_Wedge and BRepPrim_OneAxis.
    /// </summary>
    /// <param name="algo">The algorithm for which to create subshape names.</param>
    /// <param name="addNamedSubshape">The callback to add named subshapes.</param>
    public static void CreateSubshapeNames<T>(T algo, NamedSubshapeCallback addNamedSubshape) where T : class
    {
        switch (algo)
        {
            case BRepPrim_Wedge wedge:
                _CreateSubshapeNames(wedge, addNamedSubshape);
                return;

            case BRepPrim_OneAxis oneAxis:
                _CreateSubshapeNames(oneAxis, addNamedSubshape);
                return;
        }
    }

    //--------------------------------------------------------------------------------------------------

    static string[] _WedgeFaceNames;
    static string[] _WedgeEdgeNames;

    static void _CreateSubshapeNames(BRepPrim_Wedge wedge, NamedSubshapeCallback addNamedSubshape)
    {
        // Init static fields
        _WedgeFaceNames ??= Enum.GetValues<BRepPrim_Direction>().Select(d1 => string.Intern($"{d1}")).ToArray();
        _WedgeEdgeNames ??= Enum.GetValues<BRepPrim_Direction>().SelectMany(d1 => Enum.GetValues<BRepPrim_Direction>().Select(d2 =>string.Intern($"{d1}{d2}"))).ToArray();

        // Enumerate faces and edges
        foreach (var d1 in Enum.GetValues<BRepPrim_Direction>())
        {
            if (wedge.HasFace(d1))
            {
                addNamedSubshape(_WedgeFaceNames[(int)d1], wedge.Face(d1), 0, null);
            }

            foreach (var d2 in Enum.GetValues<BRepPrim_Direction>())
            {
                // Skip impossible values (e.g. XMin and XMax)
                // Check here to avoid throwing a Standard_DomainError exception in HasEdge()
                if ((int)d1 / 2 == (int)d2 / 2)
                    continue;

                if (wedge.HasEdge(d1, d2))
                {
                    addNamedSubshape(_WedgeEdgeNames[(int)d1*6+(int)d2], wedge.Edge(d1, d2), 0, null);
                }
            }
        }
    }

    //--------------------------------------------------------------------------------------------------

    static void _CreateSubshapeNames(BRepPrim_OneAxis oneAxis, NamedSubshapeCallback addNamedSubshape)
    {
        addNamedSubshape("Lateral", oneAxis.LateralFace(), 0, null);
        if (oneAxis.HasBottom())
        {
            addNamedSubshape("Bottom", oneAxis.BottomFace(), 0, null);
            if (oneAxis.HasSides())
            {
                addNamedSubshape("SideBottom", oneAxis.StartBottomEdge(), 0, null);
                addNamedSubshape("SideBottom", oneAxis.EndBottomEdge(), 1, null);
            }
        }

        var edge = oneAxis.BottomEdge();
        if (edge != null)
        {
            addNamedSubshape("Bottom", edge, 0, null);
        }

        if (oneAxis.HasTop())
        {
            addNamedSubshape("Top", oneAxis.TopFace(), 0, null);
            if (oneAxis.HasSides())
            {
                addNamedSubshape("SideTop", oneAxis.StartTopEdge(), 0, null);
                addNamedSubshape("SideTop", oneAxis.EndTopEdge(), 1, null);
            }
        }

        edge = oneAxis.TopEdge();
        if (edge != null)
        {
            addNamedSubshape("Top", edge, 0, null);
        }

        if (oneAxis.HasSides())
        {
            addNamedSubshape("Axis", oneAxis.AxisEdge(), 0, null);
            addNamedSubshape("Side", oneAxis.StartFace(), 0, null);
            addNamedSubshape("Side", oneAxis.StartEdge(), 0, null);
            addNamedSubshape("Side", oneAxis.EndFace(), 1, null);
            addNamedSubshape("Side", oneAxis.EndEdge(), 1, null);
        }
        else
        {
            var seamEdge = oneAxis.LateralWire().Edges().FirstOrDefault(e => !BRep_Tool.IsClosed(e));
            if (seamEdge != null)
            {
                addNamedSubshape("Seam", seamEdge, 0, null);
            }
        }
    }

